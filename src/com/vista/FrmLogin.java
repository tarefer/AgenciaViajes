    /**
     * Metodo para acceder a sistema
     */
    public void accesoSistema()
    {
        
        login.setUser(this.txtUser.getText());
        login.setPass(String.valueOf(jpPass.getPassword()));
        try 
        {
            String acceso = daoLogin.accesoSistema(login);
            //FrmMenuAdmin frmMenuAdmin = new FrmMenuAdmin();
            FrmMenu frmMenu = new FrmMenu();
            FrmMenuUser frmMenuUser = new FrmMenuUser();
            switch (acceso) 
            {
                case "Administrador":
                    this.dispose();
                    //frmMenuAdmin.setVisible(true);
                    //frmMenuAdmin.setTitle("Bienvenido Administrador " + login.getUser());
                    frmMenu.setVisible(true);
                    frmMenu.setTitle("Bienvenido Administrador " + login.getUser());
                    break;
                case "Cliente":
                    this.dispose();
                    frmMenuUser.setVisible(true);
                    frmMenuUser.setTitle("Bienvenido Cliente "+ login.getUser());
                    break;
                    default:
                    this.jLabelError.setVisible(true);
             }
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }

    
